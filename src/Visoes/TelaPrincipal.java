    public void recebeDadosRemocao(JTable tabela, List<Consulta> consultas) {
        int rowIndex = tabela.getSelectedRow();
        if (rowIndex != -1) {
            // Remover a consulta correspondente na lista
            consultas.remove(rowIndex);

            // Atualizar o modelo da tabela
            DefaultTableModel model = (DefaultTableModel) tabela.getModel();
            model.removeRow(rowIndex);
        } else {
            // Tratar caso nenhuma linha esteja selecionada
            System.out.println("Nenhuma linha selecionada.");
        }
    }

    public void removeLinhaTabelaElista() {
        JTable tabela = this.getjTable_tabelaConsultas();
        List<Consulta> lista = consulta.getConsultas();
        this.recebeDadosRemocao(tabela, lista);
    }

